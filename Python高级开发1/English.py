#6讲------
#主谓宾结构：动词做谓语
#主：三单，非三单
# 谓：动s,动原
# 宾：名词代词
#状：介宾，副词
#肯定句
#1,你喜欢我 - you like me(you是非三单，like用动原)
#2,她喜欢你 - she likes you(she是三单，like用动s)
#否定句
#1.他不喜欢我 - you do not like me
#2.她不喜欢你们 - she does not like me (有助动词 ，动词恢复为动原)
#一般疑问句
#1.你喜欢我吗？ - do you like me
#2.她喜欢你们吗？-does she like you
#特殊疑问句
#1.问主语语序不变
#谁喜欢我？- who likes/like me
#2.问非主语；特助动词主谓
#你喜欢谁？ -who do you like
#她喜欢谁？ - who does she like
